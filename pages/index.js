import Head from "next/head";
import Router from "next/router";
import { useEffect } from "react";
import Stripe from "stripe";
import PurchaseCard from "../components/PurchaseCard";
import { useAppContext } from "../context/CardContext";

export async function getServerSideProps(context) {
	const stripe = new Stripe(process.env.STRIPE_SECRET ?? "", {
		apiVersion: "2020-08-27",
	});

	const res = await stripe.prices.list({
		limit: 10,
		expand: ["data.product"],
	});

	const prices = res.data.filter((price) => price.active);

	return {
		props: { prices },
	};
}

export default function Home({ prices }) {
	// console.log(prices[1]);
	const { state, dispatch } = useAppContext();

	useEffect(() => {
		dispatch({
			type: "set_prices",
			value: prices,
		});
	}, [prices]);

	console.log(prices);
	return (
		<div>
			<Head>
				<title>Create Next App</title>
				<meta
					name='description'
					content='Generated by create next app'
				/>
				<link rel='icon' href='/favicon.ico' />
			</Head>

			<div className='flex flex-col space-y-4 md:flex-row md:space-y-0 md:space-x-5'>
				{prices.map((price, index) => {
					return (
						<PurchaseCard key={index} price={price}></PurchaseCard>
					);
				})}
			</div>
		</div>
	);
}
